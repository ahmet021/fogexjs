// m/d/y formatında 1/1/1600 - 12/31/9999 aralığında ki adresler
// Matches:	01.1.02 | 11-30-2001 | 2/29/2000

// d/m/y formatına çevirebiliriz.

let date2Rule = new RegExp(
  /^(?:(?:(?:0?[13578]|1[02])(\/|-|\.)31)\1|(?:(?:0?[13-9]|1[0-2])(\/|-|\.)(?:29|30)\2))(?:(?:1[6-9]|[2-9]\d)?\d{2})$|^(?:0?2(\/|-|\.)29\3(?:(?:(?:1[6-9]|[2-9]\d)?(?:0[48]|[2468][048]|[13579][26])|(?:(?:16|[2468][048]|[3579][26])00))))$|^(?:(?:0?[1-9])|(?:1[0-2]))(\/|-|\.)(?:0?[1-9]|1\d|2[0-8])\4(?:(?:1[6-9]|[2-9]\d)?\d{2})$/
);

const isDate2 = (value) => {
  return date2Rule.test(value);
};

console.log(isDate2("2/29/2000"));

